// Relational Operators
// Used for comparison.

// Result is always:

// true
// false

class RelationalOperators {

    public static void main(String args[]) {

        int a = 10;
        int b = 20;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a == b);
    }
}